package solutions.w3resource;

public class Task11 {
    public static void main(String[] args) {
        double r = 7.5;

        // Perimeter
        double p = 2 * Math.PI * r;
        System.out.println("Perimeter is = " + p);

        // Area
        double a = Math.PI * Math.pow(r, 2);
        System.out.println("Area is = " + a);
    }
}
